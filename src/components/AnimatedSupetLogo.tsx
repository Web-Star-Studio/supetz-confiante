interface AnimatedSupetLogoProps {
  width?: number;
  height?: number;
  className?: string;
  title?: string;
  /** If true, plays the animation on mount. */
  autoPlay?: boolean;
}

export const AnimatedSupetLogo = ({
  width = 230,
  height = 82,
  className,
  title = "Supet - Suplemento Animal",
  autoPlay = true,
}) => {
  const svgRef = useRef<SVGSVGElement>(null);
  const gradientId = useId().replace(/:/g, "");

  useEffect(() => {
    const svg = svgRef.current;
    if (!svg) return;

    const letters = svg.querySelectorAll<SVGGElement>(".letter-group");
    const logoGroup = svg.querySelector<SVGGElement>(".logo-group");

    if (!logoGroup) return;

    if (!autoPlay) {
      gsap.set(letters, {
        opacity: 1,
        x: 0,
        y: 0,
        scale: 1,
        rotation: 0,
        transformOrigin: "center bottom",
      });
      gsap.set(logoGroup, {
        x: 0,
        y: 0,
        rotation: 0,
        scaleY: 1,
        transformOrigin: "center bottom",
      });
      return;
    }

    // Set initial state: all letters invisible, stacked at S position.
    gsap.set(letters, {
      opacity: 0,
      x: 0,
      y: 15,
      scale: 0.7,
      rotation: 0,
      transformOrigin: "center bottom",
    });
    gsap.set(logoGroup, {
      x: 0,
      y: 0,
      rotation: 0,
      scaleY: 1,
      transformOrigin: "center bottom",
    });

    // Each letter starts offset to the left at the previous letter's position.
    letters.forEach((el, i) => {
      if (i === 0) return;

      const prevX = LETTERS[i - 1].x;
      const currX = LETTERS[i].x;
      gsap.set(el, { x: prevX - currX });
    });

    const tl = gsap.timeline();

    // ── Phase 1: S appears with a curious "sniff" bounce ─────────────
    tl.to(letters[0], {
      opacity: 1,
      y: 0,
      scale: 1,
      duration: 0.5,
      ease: "back.out(2.5)",
    });

    // Sniff wobble on S
    tl.to(
      letters[0],
      {
        rotation: -3,
        duration: 0.12,
        ease: "power2.inOut",
        yoyo: true,
        repeat: 3,
        repeatDelay: 0.02,
      },
      "-=0.15"
    );

    // ── Phase 2: Each letter detaches from the previous ──────────────
    letters.forEach((el, i) => {
      if (i === 0) return;

      // Overlap each letter's entrance with the previous one.
      const offset = "-=0.25";

      tl.to(
        el,
        {
          opacity: 1,
          x: 0,
          y: 0,
          scale: 1,
          duration: 0.45,
          ease: "back.out(1.8)",
        },
        offset
      );

      tl.to(
        el,
        {
          y: -6,
          duration: 0.1,
          ease: "power2.out",
        },
        ">-0.15"
      );
      tl.to(el, {
        y: 0,
        duration: 0.15,
        ease: "bounce.out",
      });
    });

    // ── Phase 3: Playful tail wag (whole logo sways) ─────────────────
    tl.to(logoGroup, {
      rotation: 2.5,
      x: 4,
      duration: 0.15,
      ease: "power2.inOut",
    });
    tl.to(logoGroup, {
      rotation: -2,
      x: -3,
      duration: 0.15,
      ease: "power2.inOut",
    });
    tl.to(logoGroup, {
      rotation: 1.5,
      x: 2,
      duration: 0.12,
      ease: "power2.inOut",
    });
    tl.to(logoGroup, {
      rotation: -0.8,
      x: -1,
      duration: 0.12,
      ease: "power2.inOut",
    });
    tl.to(logoGroup, {
      rotation: 0,
      x: 0,
      duration: 0.2,
      ease: "power2.out",
    });

    // ── Phase 4: Gentle breathing idle ───────────────────────────────
    tl.to(logoGroup, {
      y: -2,
      scaleY: 1.01,
      duration: 0.6,
      ease: "sine.inOut",
      yoyo: true,
      repeat: 1,
    });

    return () => {
      tl.kill();
    };
  }, [autoPlay]);

  // The SVG viewBox covers the text portion of the logo.
  return (
    <svg
      ref={svgRef}
      viewBox="100 145 230 82"
      width={width}
      height={height}
      className={className}
      xmlns="http://www.w3.org/2000/svg"
      role="img"
      aria-label={title}
      focusable="false"
      preserveAspectRatio="xMinYMid meet"
    >
      <title>{title}</title>
      <defs>
        <linearGradient id={gradientId} x1="0%" y1="0%" x2="100%" y2="100%">
          <stop offset="0%" stopColor="#FF914D" />
          <stop offset="50%" stopColor="#FF7B3A" />
          <stop offset="100%" stopColor="#FF5C37" />
        </linearGradient>
      </defs>

      <g className="logo-group">
        {LETTERS.map((letter) => (
          <g
            key={letter.id}
            className="letter-group"
            transform={`translate(${letter.x}, ${letter.y})`}
          >
            <path d={letter.d} fill={`url(#${gradientId})`} />
          </g>
        ))}
      </g>
    </svg>
  );
};

export default AnimatedSupetLogo;
