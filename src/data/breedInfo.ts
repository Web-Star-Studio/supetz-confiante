export function getPorteColor(porte: BreedDetails["porte"]): string {
  switch (porte) {
    case "Mini": return "bg-purple-100 text-purple-700 dark:bg-purple-900/30 dark:text-purple-300";
    case "Pequeno": return "bg-blue-100 text-blue-700 dark:bg-blue-900/30 dark:text-blue-300";
    case "Médio": return "bg-green-100 text-green-700 dark:bg-green-900/30 dark:text-green-300";
    case "Grande": return "bg-orange-100 text-orange-700 dark:bg-orange-900/30 dark:text-orange-300";
    case "Gigante": return "bg-red-100 text-red-700 dark:bg-red-900/30 dark:text-red-300";
  }
}

export function getEnergiaColor(energia: BreedDetails["energia"]): string {
  switch (energia) {
    case "Baixa": return "bg-sky-100 text-sky-700 dark:bg-sky-900/30 dark:text-sky-300";
    case "Moderada": return "bg-emerald-100 text-emerald-700 dark:bg-emerald-900/30 dark:text-emerald-300";
    case "Alta": return "bg-amber-100 text-amber-700 dark:bg-amber-900/30 dark:text-amber-300";
    case "Muito Alta": return "bg-rose-100 text-rose-700 dark:bg-rose-900/30 dark:text-rose-300";
  }
}
